    public function panel(Request $request)
    {
        if ($request->ajax()) {
            $modules = Module::all()->where('active', 1);

            $results = [];
            foreach ($modules as $module) {
                $results[] = ['id' => $module->id, 'name' => strtoupper(trans($module->name)), 'icon' => $module->icon];
            }
            return response()->json($results);
        }
    }

