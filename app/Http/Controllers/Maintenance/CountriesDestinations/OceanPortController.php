    /**
     * Search for records that match the parameter received.
     *
     * @param Request $request
     * @return mixed
     */
    public function autocomplete(Request $request)
    {
        if ($request->ajax()) {
            $ocean_ports = OceanPort::where(function ($query) use ($request) {
                if ($term = $request->get('term')) {
                    $query->orWhere('code', 'LIKE', $term . '%');
                    $query->orWhere('name', 'LIKE', $term . '%');
                }
            })->take(10)->get();

            $results = [];
            foreach ($ocean_ports as $ocean_port) {
                $results[] = [
                    'id' => $ocean_port->id,
                    'code' => strtoupper($ocean_port->code),
                    'name' => strtoupper($ocean_port->name),
                    'country_id' => $ocean_port->country_id,
                    'country_name' => $ocean_port->country_id > 0 ? $ocean_port->country->name : "",
                ];
            }

            return response()->json($results);
        }
    }
